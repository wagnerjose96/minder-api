package br.hela.convenio.comandos;

public class CriarConvenio {
	private String nome;
	private int ativo;

	public CriarConvenio() {

	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getAtivo() {
		return ativo;
	}

	public void setAtivo(int ativo) {
		this.ativo = ativo;
	}

}
